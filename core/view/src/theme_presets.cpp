// ── Derivation Layer ────────────────────────────────────────────────────────

Theme derive_theme(const SemanticColors& sc) {
    Theme t;

    // Background tokens
    t.colors["bg.primary"]   = sc.background;
    t.colors["bg.secondary"] = sc.secondary;
    t.colors["bg.surface"]   = sc.input;
    t.colors["bg.elevated"]  = sc.card;

    // Text tokens — derive secondary/disabled from muted
    t.colors["text.primary"]   = sc.foreground;
    t.colors["text.secondary"] = blend_colors(sc.foreground, sc.muted, 0.4f);
    t.colors["text.disabled"]  = blend_colors(sc.foreground, sc.muted, 0.7f);
    t.colors["text.link"]      = sc.primary;

    // Accent tokens
    t.colors["accent.primary"]   = sc.primary;
    t.colors["accent.secondary"] = sc.accent;
    t.colors["accent.error"]     = sc.destructive;
    t.colors["accent.warning"]   = shift_hue(sc.primary, 30.0f);   // warm hue shift
    t.colors["accent.success"]   = shift_hue(sc.primary, -120.0f); // green hue shift

    // Control tokens
    t.colors["control.track"]  = sc.muted;
    t.colors["control.fill"]   = sc.primary;
    t.colors["control.thumb"]  = sc.foreground;
    t.colors["control.border"] = sc.border;

    // Knob tokens (derived from accent/primary)
    t.colors["knob.arc"]    = sc.primary;
    t.colors["knob.arc.bg"] = sc.muted;
    t.colors["knob.thumb"]  = sc.foreground;

    // Slider tokens
    t.colors["slider.track"] = sc.muted;
    t.colors["slider.fill"]  = sc.primary;
    t.colors["slider.thumb"] = sc.foreground;

    // Meter tokens
    t.colors["meter.green"]  = shift_hue(sc.primary, -120.0f);
    t.colors["meter.yellow"] = shift_hue(sc.primary, 30.0f);
    t.colors["meter.red"]    = sc.destructive;

    // Waveform tokens
    t.colors["waveform.line"] = sc.primary;
    t.colors["waveform.fill"] = with_alpha(sc.primary, 80);
    t.colors["waveform.grid"] = blend_colors(sc.muted, sc.background, 0.5f);

    // Card tokens
    t.colors["card.empty"]   = sc.card;
    t.colors["card.loading"] = blend_colors(sc.card, sc.muted, 0.3f);
    t.colors["card.ready"]   = sc.card;
    t.colors["card.error"]   = with_alpha(sc.destructive, 40);

    // Overlay tokens
    t.colors["overlay.bg"]    = with_alpha(sc.background, 180);
    t.colors["modal.bg"]      = sc.card;
    t.colors["modal.border"]  = sc.border;
    t.colors["tooltip.bg"]    = sc.foreground;
    t.colors["tooltip.text"]  = sc.background;

    // Tab tokens
    t.colors["tab.active"]   = sc.primary;
    t.colors["tab.inactive"] = blend_colors(sc.foreground, sc.muted, 0.6f);

    // Effects tokens
    t.colors["divider"]        = sc.border;
    t.colors["focus.ring"]     = sc.ring;
    t.colors["progress.track"] = sc.muted;
    t.colors["progress.fill"]  = sc.primary;
    t.colors["spinner"]        = sc.primary;

    // Gradient tokens
    t.colors["gradient.start"] = sc.primary;
    t.colors["gradient.end"]   = sc.secondary;

    // ── Dimensions (same for all presets — use Pulp defaults) ───────────────

    t.dimensions["spacing.xs"]   = 2.0f;
    t.dimensions["spacing.sm"]   = 4.0f;
    t.dimensions["spacing.md"]   = 8.0f;
    t.dimensions["spacing.lg"]   = 16.0f;
    t.dimensions["spacing.xl"]   = 24.0f;

    t.dimensions["radius.sm"]    = 4.0f;
    t.dimensions["radius.md"]    = 8.0f;
    t.dimensions["radius.lg"]    = 12.0f;
    t.dimensions["radius.full"]  = 9999.0f;

    t.dimensions["font.xs"]      = 10.0f;
    t.dimensions["font.sm"]      = 12.0f;
    t.dimensions["font.md"]      = 14.0f;
    t.dimensions["font.lg"]      = 18.0f;
    t.dimensions["font.xl"]      = 24.0f;
    t.dimensions["line_height.sm"] = 16.0f;
    t.dimensions["line_height.md"] = 20.0f;
    t.dimensions["line_height.lg"] = 28.0f;

    t.dimensions["control.knob_size"]   = 48.0f;
    t.dimensions["control.fader_width"] = 24.0f;
    t.dimensions["control.meter_width"] = 12.0f;

    t.dimensions["motion.duration.fast"]        = 0.08f;
    t.dimensions["motion.duration.normal"]      = 0.15f;
    t.dimensions["motion.duration.slow"]        = 0.30f;
    t.dimensions["motion.duration.meter_decay"] = 0.30f;
    t.dimensions["motion.duration.peak_hold"]   = 1.50f;

    // ── Strings ─────────────────────────────────────────────────────────────

    t.strings["font.family"] = "Inter";
    t.strings["font.mono"]   = "JetBrains Mono";

    t.strings["motion.easing.interaction"] = "ease_out_cubic";
    t.strings["motion.easing.enter"]       = "ease_out_quad";
    t.strings["motion.easing.exit"]        = "ease_in_quad";

    return t;
}

Theme theme_from_preset(const ThemePreset& preset, bool dark) {
    const auto& sc = dark ? preset.dark : preset.light;
    auto theme = derive_theme(sc);

    // Apply explicit overrides if any
    const auto& overrides = dark ? preset.dark_overrides : preset.light_overrides;
    theme.apply_overrides(overrides);

    return theme;
}

