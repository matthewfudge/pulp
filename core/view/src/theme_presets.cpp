static const std::vector<ThemePreset>& presets_instance() {
    static auto presets = build_presets();
    return presets;
}

const std::vector<ThemePreset>& all_presets() {
    return presets_instance();
}

const ThemePreset* find_preset(const std::string& id) {
    for (auto& p : presets_instance()) {
        if (p.id == id) return &p;
    }
    return nullptr;
}

std::vector<std::string> preset_ids() {
    std::vector<std::string> ids;
    ids.reserve(presets_instance().size());
    for (auto& p : presets_instance())
        ids.push_back(p.id);
    return ids;
}

