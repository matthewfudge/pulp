// WinRT MIDI 2.0 (UMP) backend for Windows 11.
//
// The Windows MIDI Services SDK's `Microsoft.Windows.Devices.Midi2`
// projection exposes native MIDI 2.0 endpoints (Universal MIDI Packets),
// `MidiEndpointDeviceWatcher` hotplug, and high-resolution `MidiClock`
// timestamps. The legacy mmeapi backend in winmidi_device.cpp truncates
// MIDI 2.0 UMP, has no hotplug, and requires manual SysEx reassembly. This
// TU is the real WinRT consumer.
//
// Opt-in: it is only compiled when PULP_HAS_WINRT_MIDI is set — a build flag
// the developer enables (see core/midi/CMakeLists.txt) after provisioning the
// Windows MIDI Services SDK's Microsoft.Windows.Devices.Midi2 C++/WinRT
// projection (preferred: the official vcpkg port; see tools/ci/midi2/).
// Without the flag, the linker never sees this object and the mmeapi backend
// stays authoritative.
//
// Why gate rather than auto-detect at compile time: the Midi2 projection
// headers are generated by cppwinrt from a per-SDK-version `.winmd` and ship
// out-of-band with the Windows MIDI Services SDK rather than the base Windows
// SDK. Compile-time opt-in keeps the default CI matrix free of that
// out-of-band dependency.

